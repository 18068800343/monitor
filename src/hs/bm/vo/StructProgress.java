package hs.bm.vo;


public class StructProgress {

	private String struct_id;
	private String struct_mode;
	private boolean struct_eva;
	private boolean struct_chk;
	private int member_all;
	private int member_chk;
	

	public String getStruct_id() {
		return struct_id;
	}

	public void setStruct_id(String struct_id) {
		this.struct_id = struct_id;
	}

	public String getStruct_mode() {
		return struct_mode;
	}

	public void setStruct_mode(String struct_mode) {
		this.struct_mode = struct_mode;
	}

	public boolean isStruct_eva() {
		return struct_eva;
	}

	public void setStruct_eva(boolean struct_eva) {
		this.struct_eva = struct_eva;
	}

	public boolean isStruct_chk() {
		return struct_chk;
	}

	public void setStruct_chk(boolean struct_chk) {
		this.struct_chk = struct_chk;
	}

	public int getMember_all() {
		return member_all;
	}

	public void setMember_all(int member_all) {
		this.member_all = member_all;
	}

	public int getMember_chk() {
		return member_chk;
	}

	public void setMember_chk(int member_chk) {
		this.member_chk = member_chk;
	}

	
	
}
